CREATE TABLE Users(
userId INT(11) AUTO_INCREMENT,
adminId INT(11) NOT NULL,
username VARCHAR(45) NOT NULL,
pass VARCHAR(45) NOT NULL,
email VARCHAR(45) NOT NULL,
balance DOUBLE(11,2) DEFAULT 100.00,
PRIMARY KEY (UserId)
)
ENGINE = INNODB;

CREATE TABLE Orders(
id INT(11) AUTO_INCREMENT,
userId INT,
orderDate DATETIME DEFAULT CURRENT_TIMESTAMP,
PRIMARY KEY (id),
KEY userId (userId),
CONSTRAINT Orders_fk_1 FOREIGN KEY(userId) REFERENCES Users (userId)
ON DELETE CASCADE
ON UPDATE CASCADE)
ENGINE = INNODB;


CREATE TABLE Bottoms(
id INT(11) AUTO_INCREMENT,
flavour VARCHAR(45) NOT NULL,
price DOUBLE(11,2) NOT NULL,
PRIMARY KEY (id)
)
ENGINE = INNODB;

CREATE TABLE Toppings( 
id INT(11) AUTO_INCREMENT,
flavour VARCHAR(45) NOT NULL,
price DOUBLE(11,2) NOT NULL,
PRIMARY KEY (id)
)
ENGINE = INNODB;

CREATE TABLE OrderLines(
id INT (11) AUTO_INCREMENT,
amount INT (11) NOT NULL,
price DOUBLE(11,2) NOT NULL,
orderId INT,
bottomId INT,
toppingId INT,
PRIMARY KEY(id),
KEY orderId(orderId), KEY bottomId(bottomId), KEY toppingId(toppingId),
CONSTRAINT orderlines_fk_1 FOREIGN KEY(orderId) REFERENCES Orders(id),
CONSTRAINT orderlines_fk_2 FOREIGN KEY(bottomId) REFERENCES Bottoms(id),
CONSTRAINT orderlines_fk_3 FOREIGN KEY (toppingId) REFERENCES Toppings(id)
ON DELETE CASCADE
ON UPDATE CASCADE)
ENGINE = INNODB;

CREATE TABLE UnfinishedOrders(
id INT (11) AUTO_INCREMENT,
