CONSTRAINT unfinishedorders_fk_2 FOREIGN KEY(bottomId)REFERENCES Bottoms (id),
CONSTRAINT unfinishedorders_fk_3 FOREIGN KEY(toppingId) REFERENCES Toppings(id)
ON DELETE CASCADE
ON UPDATE CASCADE)
ENGINE = INNODB;



